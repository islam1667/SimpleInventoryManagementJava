    public double sumAndShow() {
        double sum = 0;
        for (int row = 0; row < jTableSell.getRowCount() - 1; row++) {
            sum += (double) jTableSell.getValueAt(row, Config.TOTAL_COL);
        }
        totalJLabel.setText("Total: " + String.format("%.2f", sum));
        return sum;
    }

